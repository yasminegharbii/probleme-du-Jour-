

// 1- Crée une fonction qui affiche les nombres de 0 à 10 avec while. (console.log()) :) 
function Numbers() {
    var s = 0;
    while (s < 10) {
        console.log(s);
    }
}



// 2- Crée une fonction qui prend un nombre et affiche les nombres de ce nombre jusqu’à 0 avec while. 

function TilZero(n) {
    var x = n
    while (x > 0) {
        console.log(n);
    }
}



